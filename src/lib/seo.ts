// ─── Article JSON-LD builder ──────────────────────────────────────────────────

interface ArticleSeoFields {
  titulo: string
  resumo: string
  slug: string
  publicado_em: string
  atualizado_em: string
  autor?: string
  imagem_url?: string | null
}

/**
 * Builds an Article JSON-LD object for a content hub article page.
 * Uses Schema.org Article type with author fallback to the organization.
 *
 * Embed as:
 *   <script type="application/ld+json">{JSON.stringify(result)}</script>
 */
export function buildArticleJsonLd(article: ArticleSeoFields): object {
  const articleUrl = canonicalUrl(`/conteudos/${article.slug}`)
  return {
    "@context": "https://schema.org",
    "@type": "Article",
    "@id": articleUrl,
    headline: article.titulo,
    description: article.resumo,
    url: articleUrl,
    datePublished: article.publicado_em,
    dateModified: article.atualizado_em,
    author: {
      "@type": article.autor ? "Person" : "Organization",
      name: article.autor ?? siteConfig.fullName,
      ...(article.autor ? {} : { url: siteConfig.url }),
    },
    publisher: {
      "@type": "Organization",
      name: siteConfig.fullName,
      url: siteConfig.url,
    },
    ...(article.imagem_url ? { image: article.imagem_url } : {}),
    inLanguage: "pt-BR",
    isPartOf: {
      "@type": "WebSite",
      name: siteConfig.name,
      url: siteConfig.url,
    },
  }
}

