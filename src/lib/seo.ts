// ─── Open Graph helper ────────────────────────────────────────────────────────

type OgType = "website" | "article"

interface PageOpenGraphOptions {
  title: string
  description: string
  /** Absolute URL path starting with "/", e.g. "/sobre" */
  path: string
  /** Falls back to DEFAULT_OG_IMAGE when omitted */
  imageUrl?: string | null
  type?: OgType
}

/**
 * Builds a consistent openGraph object for any public marketing page.
 * Always includes the default OG image so social previews never go blank.
 */
export function pageOpenGraph({
  title,
  description,
  path,
  imageUrl,
  type = "website",
}: PageOpenGraphOptions) {
  return {
    type,
    title,
    description,
    siteName: siteConfig.name,
    url: canonicalUrl(path),
    locale: "pt_BR",
    images: [
      {
        url: imageUrl ?? DEFAULT_OG_IMAGE,
        width: 1200,
        height: 630,
        alt: title,
      },
    ],
  }
}

