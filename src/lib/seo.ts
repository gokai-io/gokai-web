// ─── LocalBusiness JSON-LD builder ───────────────────────────────────────────

interface LocalBusinessSeoFields {
  /** City name, e.g. "São Paulo" */
  cidade: string
  /** Neighborhood, e.g. "Pinheiros" (optional) */
  bairro?: string
  /** State abbreviation, e.g. "SP" */
  estado: string
  /** Full postal address (only include when real — omit if uncertain) */
  endereco?: string
  /** E.164 phone number (sourced from site.ts CONTACT_PHONE_HREF) */
  telefone?: string | null
  /** Contact email */
  email?: string | null
  /** Google Maps URL or directions link */
  mapaUrl?: string
  /** URL slug for this location page, e.g. "sao-paulo" */
  slug: string
}

/**
 * Builds a LocalBusiness / SportsClub JSON-LD object for a location landing page.
 *
 * Schema.org SportsClub is a subtype of LocalBusiness — it improves eligibility
 * for Google's local knowledge panel and maps integration.
 *
 * Only include this on confirmed location pages with real address data.
 * Omit `endereco`, `telefone`, and `email` by passing null/undefined — no fake
 * data will be emitted.
 *
 * Embed the result as:
 *   <script type="application/ld+json">{JSON.stringify(result)}</script>
 */
export function buildLocalBusinessJsonLd(fields: LocalBusinessSeoFields): object {
  const pageUrl = canonicalUrl(`/localidades/${fields.slug}`)
  const locationName = fields.bairro
    ? `${siteConfig.name} – ${fields.bairro}, ${fields.cidade}`
    : `${siteConfig.name} – ${fields.cidade}`

  return {
    "@context": "https://schema.org",
    "@type": "SportsClub",
    "@id": pageUrl,
    name: locationName,
    alternateName: siteConfig.fullName,
    url: pageUrl,
    parentOrganization: {
      "@type": "SportsOrganization",
      name: siteConfig.fullName,
      url: siteConfig.url,
    },
    // Address — only emitted when a confirmed address is supplied
    ...(fields.endereco
      ? {
          address: {
            "@type": "PostalAddress",
            streetAddress: fields.endereco,
            addressLocality: fields.cidade,
            addressRegion: fields.estado,
            addressCountry: "BR",
          },
        }
      : {}),
    // Contact — only emitted when real data is available
    ...(fields.telefone ? { telephone: fields.telefone } : {}),
    ...(fields.email ? { email: fields.email } : {}),
    // Maps link
    ...(fields.mapaUrl ? { hasMap: fields.mapaUrl } : {}),
    inLanguage: "pt-BR",
  }
}
