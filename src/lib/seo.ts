// ─── Event JSON-LD builder ────────────────────────────────────────────────────

interface EventSeoFields {
  titulo: string
  descricao: string | null
  data_inicio: string
  data_fim?: string | null
  local?: string | null
  imagem_url?: string | null
  slug: string
  isPast?: boolean
}

/**
 * Builds an Event JSON-LD object from available event fields.
 * Only includes optional fields (location, image, endDate) when present.
 * Adds an @id equal to the canonical URL so search engines can deduplicate
 * the entity across pages.
 *
 * Embed the result as:
 *   <script type="application/ld+json">{JSON.stringify(result)}</script>
 */
export function buildEventJsonLd(evento: EventSeoFields): object {
  const eventUrl = canonicalUrl(`/eventos/${evento.slug}`)
  return {
    "@context": "https://schema.org",
    "@type": "Event",
    "@id": eventUrl,
    name: evento.titulo,
    ...(evento.descricao ? { description: evento.descricao } : {}),
    startDate: evento.data_inicio,
    ...(evento.data_fim ? { endDate: evento.data_fim } : {}),
    url: eventUrl,
    organizer: {
      "@type": "Organization",
      name: siteConfig.fullName,
      url: siteConfig.url,
    },
    ...(evento.local
      ? {
          location: {
            "@type": "Place",
            name: evento.local,
          },
        }
      : {}),
    ...(evento.imagem_url ? { image: evento.imagem_url } : {}),
    eventStatus: evento.isPast
      ? "https://schema.org/EventCompleted"
      : "https://schema.org/EventScheduled",
    eventAttendanceMode: "https://schema.org/OfflineEventAttendanceMode",
  }
}

// ─── Breadcrumb JSON-LD builder ───────────────────────────────────────────────

interface BreadcrumbItem {
  name: string
  /** Absolute canonical URL — omit for the final (current) item */
  url?: string
}

/**
 * Builds a BreadcrumbList JSON-LD object.
 * Improves rich-result eligibility for detail pages.
 * Embed alongside Event or other entity JSON-LD as a second script block.
 *
 * @example
 *   buildBreadcrumbJsonLd([
 *     { name: "Eventos", url: canonicalUrl("/eventos") },
 *     { name: ev.titulo },
 *   ])
 */
export function buildBreadcrumbJsonLd(items: BreadcrumbItem[]): object {
  return {
    "@context": "https://schema.org",
    "@type": "BreadcrumbList",
    itemListElement: items.map((item, index) => ({
      "@type": "ListItem",
      position: index + 1,
      name: item.name,
      ...(item.url ? { item: item.url } : {}),
    })),
  }
}

